from qgis.core import QgsProject, QgsLayerTreeGroup, QgsLayerTreeLayer, QgsSettings

from . import native3d, styling
from .osm_download import BASE_DEPTH_M

_S = "osm_quick_3d"


class PluginDockWidget(QDockWidget):
    def __init__(self, iface, parent=None):
        super().__init__("OSM Quick 3D Controller", parent)
        self.iface = iface
        self.setObjectName("OSMQuick3DController")
        self.setAllowedAreas(Qt.DockWidgetArea.LeftDockWidgetArea | Qt.DockWidgetArea.RightDockWidgetArea)
        
        self._build_ui()
        
        # Restore saved resolution settings
        s = QgsSettings()
        try:
            res_val = int(s.value(f"{_S}/map_resolution", 1024))
            ridx = self.map_resolution.findData(res_val)
            if ridx >= 0:
                self.map_resolution.blockSignals(True)
                self.map_resolution.setCurrentIndex(ridx)
                self.map_resolution.blockSignals(False)
        except (TypeError, ValueError):
            pass
        try:
            class_val = s.value(f"{_S}/classification", "continuous")
            clidx = self.classification.findData(class_val)
            if clidx >= 0:
                self.classification.blockSignals(True)
                self.classification.setCurrentIndex(clidx)
                self.classification.blockSignals(False)
        except (TypeError, ValueError):
            pass
            
        self.refresh_groups()
        
